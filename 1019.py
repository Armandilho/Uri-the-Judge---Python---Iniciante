tempo = int(input())
contadorhora = 0
contadorminuto = 0
contadorsegundo = 0

while(tempo>=3600):
    (tempo-3600)
    tempo = tempo-3600
    contadorhora = contadorhora+1
while(tempo>=60):
    (tempo-60)
    tempo = tempo-60
    contadorminuto = contadorminuto+1
while(tempo>=1):
    (tempo-1)
    tempo = tempo-1
    contadorsegundo = contadorsegundo+1


print("%i:%i:%i" %(contadorhora, contadorminuto, contadorsegundo))
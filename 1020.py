dias = int(input())
contadorAno = 0
contadorMes = 0
contadorDia = 0

while(dias>=365): #contador de Anos#
    (dias-365)
    dias=dias-365
    contadorAno = contadorAno+1
while(dias>=30): #contador de meses#
    (dias-30)
    dias=dias-30
    contadorMes = contadorMes+1
while(dias>=1): #contador de dias#
    (dias-1)
    dias=dias-1
    contadorDia = contadorDia+1

print("%i ano(s)" %contadorAno)
print("%i mes(es)" %contadorMes)
print("%i dia(s)" %contadorDia)
    